bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {

// ┌─────────────────────────────────────────────────┐
// │ q m k                                           │
// └─────────────────────────────────────────────────┘

        case MAKE_H:
          if (record->event.pressed) {
            SEND_STRING ("qmk compile -kb totem -km castares");
            tap_code(KC_ENTER);
          }
          break;

// ┌─────────────────────────────────────────────────┐
// │ a r r o w   m a c r o s                         │
// └─────────────────────────────────────────────────┘

        case ARROW:
            if (record->event.pressed) {
                SEND_STRING("->");
            }
            return false;

        case RSHIFT_OP:
            if (record->event.pressed) {
                SEND_STRING(">>");
            }
            return false;
    }
    return true;
}
